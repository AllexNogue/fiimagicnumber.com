        $('.btn-calc').on('click', function() {
            $('#num-cotas').html('000');
            $('#show').css('display', 'none');

            if ($('#ativo').val() == '') {
                alert("Preencha o valor do Ativo");
                return 1;
            }
            if ($('#dividendo').val() == '') {
                alert("Preencha o valor do Dividendo");
                return 1;
            }

            var ativo = parseFloat($('#ativo').val().replace(",", "."));
            var dividendo = parseFloat($('#dividendo').val().replace(",", "."));

            var cotas = Math.ceil(ativo / dividendo);

            if (cotas > 0) {
                $('#num-cotas').html(cotas);
                $('#show').css('display', 'block');
            }
        })
